	<script>
		$(document).ready(function(){
			block = $('.news-item.blocks__block');
			bname = $('.news-item.on');
			content = $('.news-content__content');
			closeBtn = $('.news-content__content .back-news-btn');
			wHeight = $(window).outerHeight();
			wWidth = $(window).outerWidth();
			bHeight = block.outerHeight();
			bWidth = block.outerWidth();
			xVal = Math.round(wWidth / bWidth) * 2;
			yVal = wHeight / bHeight * 2;

			expand = function() {
			  var aBlock, num;
			  num = $(this).index();
			  aBlock = block.eq(num);
			  if (!aBlock.hasClass('active')) {
			    bname.css('opacity', '0');
			    aBlock.css({
			      'transform': 'scale(' + xVal + ',' + yVal + ')',
			      '-webkit-transform': 'scale(' + xVal + ',' + yVal + ')'
			    });
			    aBlock.addClass('active');
			    openContent(num);
			    $('.news-content').css('z-index', '101');
		    	setTimeout(function(){
			  		$('body').addClass('oh');
		    	}, 700);
			    return false
			  }
			};

			openContent = function(num) {
			  var aContent;
			  content.css({
			    'transition': 'all 0.2s 0.2s ease-out',
			    '-webkit-transition': 'all 0.2s 0.2s ease-out'
			  });
			  aContent = content.eq(num);
			  setTimeout(function(){
			  	aContent.addClass('active');
			  },500);
			  $('.news-items.blocks').css('z-index','100');
			};

			closeContent = function() {
			  bname.css('opacity', '1');
			  content.css({
			    'transition': 'all 0.1s 0 ease-out',
			    '-webkit-transition': 'all 0.1s 0 ease-out'
			  });
			  block.css({
			    'transform': 'scale(1)',
			    '-webkit-transform': 'scale(1)'
			  });
			  block.removeClass('active');
			  content.removeClass('active');

			  $('body').removeClass('oh');
			  $('.news-content').css('z-index', '-1');
			  $('.news-items.blocks').css('z-index','0');

			  return false
			};

			updateValues = function() {
			  var yVal;
			  var xVal;
			  var bWidth;
			  var bHeight;
			  var wWidth;
			  var wHeight;
			  var aBlock;
			  if (block.hasClass('active')) {
			    aBlock = $('.news-item.blocks__block.active');
			    wHeight = $(window).height();
			    wWidth = $(window).width();
			    bHeight = block.height();
			    bWidth = block.width();
			    xVal = Math.round(wWidth / bWidth) + 0.03;
			    yVal = wHeight / bHeight + 0.03;
			    aBlock.css({
			      'transform': 'scale(' + xVal + ',' + yVal + ')',
			      '-webkit-transform': 'scale(' + xVal + ',' + yVal + ')'
			    });
			  }
			};

			$(window).on('resize', updateValues);

			bname.on('click', expand);

			closeBtn.on('click', closeContent);
		});
	</script>
